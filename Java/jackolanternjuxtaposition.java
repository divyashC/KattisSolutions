// https://open.kattis.com/problems/jackolanternjuxtaposition

import java.util.Scanner;

public class Kattis {

	public static void main(String[] args) {
		
		int n, t, m;

		Scanner sc = new Scanner(System.in);
		
		n = sc.nextInt();
		t = sc.nextInt();
		m = sc.nextInt();
		
		sc.close();
		
		System.out.println(n*t*m);
		
		
		
	}

}